package com.mycompany.myapplication.dto;

public class Logo {
    private int lno;
    private String lname;
    private int lphoto;
    private int lstar;

    public int getLno() {
        return lno;
    }

    public void setLno(int lno) {
        this.lno = lno;
    }

    public String getLname() {
        return lname;
    }

    public void setLname(String lname) {
        this.lname = lname;
    }

    public int getLphoto() {
        return lphoto;
    }

    public void setLphoto(int lphoto) {
        this.lphoto = lphoto;
    }

    public int getLstar() {
        return lstar;
    }

    public void setLstar(int lstar) {
        this.lstar = lstar;
    }


}
